public class IfElse {
    public static void main(String args[]){
        int x = 30;

        if(x == 10){
            System.out.println("Value of x is 10");
        }else if(x > 20){
            System.out.println("Value of x is > 20");

        }else if( x < 15){
            System.out.println("Value of x is < 15");
        }else if(x < 13){
            System.out.println("Value is < 13");
        }else if(x > 17){
            System.out.println("Value of x is > 17");
        }else{
            System.out.println("X is not equal to 10");
        }
    }
}
